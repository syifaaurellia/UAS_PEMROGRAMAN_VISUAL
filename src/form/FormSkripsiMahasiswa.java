private void hapusData() {
int row = tbl_skripsi.getSelectedRow();
if (row == -1) {
JOptionPane.showMessageDialog(this, "Pilih baris yang akan di hapus !");
return;
}
int jawab = JOptionPane.showOptionDialog(this,
"Apakah Anda ingin menghapus data ini?",
"Konfirmasi",
JOptionPane.YES_NO_OPTION,
JOptionPane.QUESTION_MESSAGE, null, null, null);
if (jawab == JOptionPane.YES_OPTION) {
try {
String sql = "DELETE FROM tbl_skripsi WHERE nim='" +
tfNim.getText() + "' ";
pst = conn.prepareStatement(sql);
pst.executeUpdate();
JOptionPane.showMessageDialog(null, "Data berhasil di hapus");
clearForm();
tampilData();
} catch (Exception e) {
JOptionPane.showMessageDialog(this, e.getMessage());
}
} else {
clearForm();
tampilData();

}
}    
// Tambahkan method ini di akhir file atau sebelum method `main`
private void tampilData() {
    DefaultTableModel kolomtabel = new DefaultTableModel();
    kolomtabel.addColumn("No.");
    kolomtabel.addColumn("Nama");
    kolomtabel.addColumn("NIM");
    kolomtabel.addColumn("Prodi");
    kolomtabel.addColumn("Judul Skripsi");
    kolomtabel.addColumn("Dosen Pembimbing");
    kolomtabel.addColumn("Progress Skripsi");
    kolomtabel.addColumn("Status");

    try {
        Connection conn = KoneksiDB.getKoneksi();
        Statement st = conn.createStatement();
        String sql = "SELECT * FROM tbl_skripsi";
        ResultSet rs = st.executeQuery(sql);
        int nomor = 1;

        while (rs.next()) {
            kolomtabel.addRow(new Object[]{
                ("" + nomor++),
                rs.getString("nama"),
                rs.getString("nim"),
                rs.getString("prodi"),
                rs.getString("judul_skripsi"),
                rs.getString("dosen_pembimbing"),
                rs.getString("progress_skripsi"),
                rs.getString("status")
            });
        }
        tbl_skripsi.setModel(kolomtabel);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal menampilkan data: " + e.getMessage());
    }
}

