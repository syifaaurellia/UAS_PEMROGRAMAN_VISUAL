    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
              if (tfNama.getText().equals("") || tfNim.getText().equals("") ||
        tfDosenPembimbing.getText().equals("") || tfJudulSkripsi.getText().equals("") ||
        taProgressSkripsi.getText().equals("") || buttonGroup1.getSelection() == null ||
        cmbProdi.getSelectedItem().equals("-- Program Studi --")) {
        
        JOptionPane.showMessageDialog(this, "Field harap diisi!", "Validasi", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        Connection conn = KoneksiDB.getKoneksi();
        Statement st = conn.createStatement();
        String status = rbSedangDikerjakan.isSelected() ? "Sedang Dikerjakan" : "Selesai";
        String sql = "INSERT INTO tbl_skripsi (nama, nim, prodi, judul_skripsi, dosen_pembimbing, progress_skripsi, status) VALUES ('" 
              + tfNama.getText() + "', '" 
              + tfNim.getText() + "', '" 
              + tfDosenPembimbing.getText() + "', '" 
              + status + "', '" 
              + cmbProdi.getSelectedItem() + "', '" 
              + tfJudulSkripsi.getText() + "', '" 
              + taProgressSkripsi.getText() + "')";

        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        tampilData();
        clearForm();
    } catch ( HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
                                 
    }                       
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         if (tbl_skripsi.getSelectedRow() == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin diupdate!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        String status = rbSedangDikerjakan.isSelected() ? "Sedang Dikerjakan" : "Selesai";
        String sql = "UPDATE tbl_skripsi SET nama=?, prodi=?, judul_skripsi=?, dosen_pembimbing=?, progress_skripsi=?, status=? WHERE nim=?";
        
        // Pastikan koneksi (conn) sudah terinisialisasi dan terbuka
        try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
            pstmt.setString(1, tfNama.getText());
            pstmt.setString(2, tfDosenPembimbing.getText());
            pstmt.setString(3, status);
            pstmt.setString(4, (String) cmbProdi.getSelectedItem());
            pstmt.setString(5, tfJudulSkripsi.getText());
            pstmt.setString(6, taProgressSkripsi.getText());
            pstmt.setString(7, tfNim.getText());
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
            tampilData();
            clearForm();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

